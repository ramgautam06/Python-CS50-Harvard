# Integer 
x1 = int(input("What's x? "))
y1 = int(input("What's y? "))
print(f"The sum of x {x1} and y {y1}  is {x1+y1}")

#Float 
x2= float(input("What's x? "))
y2 = float(input("What's y? "))
z2 = round(x2+y2)
k2 = round(x2/y2, 2)
print(f"The sum of x {x2} and y {y2}  is {z2:,}")
print(f"The division of x {x2} and y {y2}  is {k2:.2f}")